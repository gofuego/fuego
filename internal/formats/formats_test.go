// The materializer copies schema.md (required), DEPENDENCIES.md (optional),
// and the testdata tree, making module-cache read-only files writable.
func TestMaterializeFromDir(t *testing.T) {
	pkg := t.TempDir()
	os.WriteFile(filepath.Join(pkg, "schema.md"), []byte("# x — parser contract\n"), 0444)
	os.MkdirAll(filepath.Join(pkg, "testdata"), 0755)
	os.WriteFile(filepath.Join(pkg, "testdata", "a.x"), []byte("in"), 0444)
	os.WriteFile(filepath.Join(pkg, "testdata", "a.golden.json"), []byte("{}"), 0444)

	project := t.TempDir()
	f := Format{Name: "x", ImportPath: "example.com/x", Symbol: "x"}
	if err := materializeFromDir(pkg, project, f); err != nil {
		t.Fatal(err)
	}

	for _, rel := range []string{"docs/formats/x/schema.md", "docs/formats/x/testdata/a.x", "docs/formats/x/testdata/a.golden.json"} {
		p := filepath.Join(project, filepath.FromSlash(rel))
		info, err := os.Stat(p)
		if err != nil {
			t.Errorf("missing %s: %v", rel, err)
			continue
		}
		if info.Mode().Perm()&0200 == 0 {
			t.Errorf("%s copied read-only", rel)
		}
	}

	// No schema.md → an error naming the format.
	empty := t.TempDir()
	if err := materializeFromDir(empty, project, f); err == nil || !strings.Contains(err.Error(), "schema.md") {
		t.Errorf("want schema.md error, got %v", err)
	}
}

func TestWriteIndex(t *testing.T) {
	project := t.TempDir()
	fmts := []Format{
		{Name: "openapi", ImportPath: "github.com/gofuego/fuego-formats/openapi", Symbol: "openapi"},
		{Name: "markdown", ImportPath: "github.com/gofuego/fuego/parsers/markdown", Symbol: "markdown"},
	}
	// openapi has fixtures on disk; markdown doesn't (yet).
	os.MkdirAll(filepath.Join(project, "docs", "formats", "openapi", "testdata"), 0755)

	if err := WriteIndex(project, fmts); err != nil {
		t.Fatal(err)
	}
	idx, err := os.ReadFile(filepath.Join(project, "docs", "formats", "README.md"))
	if err != nil {
		t.Fatal(err)
	}
	got := string(idx)
	// Sorted by name, fixtures linked only when present.
	if !strings.Contains(got, "[schema.md](openapi/schema.md)") || !strings.Contains(got, "[testdata/](openapi/testdata)") {
		t.Errorf("openapi row wrong:\n%s", got)
	}
	if !strings.Contains(got, "[schema.md](markdown/schema.md)") {
		t.Errorf("markdown row missing:\n%s", got)
	}
	if strings.Index(got, "| markdown |") > strings.Index(got, "| openapi |") {
		t.Errorf("index not sorted by name:\n%s", got)
	}
}
