// The resolver matrix: the markdown alias, convention short names, full
// third-party paths, and the failure modes. No hardcoded module list beyond
// the alias — a short name resolves whether or not the module exists (go get
// reports typos).
func TestResolve(t *testing.T) {
	tests := []struct {
		name    string
		symbol  string
		want    Format
		wantErr string
	}{
		{name: "markdown", want: Format{Name: "markdown", ImportPath: "github.com/gofuego/fuego/parsers/markdown", Symbol: "markdown"}},
		{name: "openapi", want: Format{Name: "openapi", ImportPath: "github.com/gofuego/fuego-formats/openapi", Symbol: "openapi"}},
		{name: "notyetinvented", want: Format{Name: "notyetinvented", ImportPath: "github.com/gofuego/fuego-formats/notyetinvented", Symbol: "notyetinvented"}},
		{name: "github.com/acme/terraform", want: Format{Name: "terraform", ImportPath: "github.com/acme/terraform", Symbol: "terraform"}},
		{name: "github.com/acme/fuego-terraform", wantErr: "--symbol"},
		{name: "github.com/acme/fuego-terraform", symbol: "terraform", want: Format{Name: "fuego-terraform", ImportPath: "github.com/acme/fuego-terraform", Symbol: "terraform"}},
		{name: "bad-short", wantErr: "not a valid Go identifier"},
		{name: "", wantErr: "empty"},
		{name: "openapi", symbol: "not-ident", wantErr: "not a valid Go identifier"},
	}
	for _, tt := range tests {
		got, err := ResolveWithSymbol(tt.name, tt.symbol)
		if tt.wantErr != "" {
			if err == nil || !strings.Contains(err.Error(), tt.wantErr) {
				t.Errorf("Resolve(%q, %q) error = %v, want containing %q", tt.name, tt.symbol, err, tt.wantErr)
			}
			continue
		}
		if err != nil {
			t.Errorf("Resolve(%q, %q): %v", tt.name, tt.symbol, err)
			continue
		}
		if got != tt.want {
			t.Errorf("Resolve(%q, %q) = %+v, want %+v", tt.name, tt.symbol, got, tt.want)
		}
	}
}

// RenderFile → ParseFile must round-trip, including named imports for
// symbols that differ from the path base and the markdown alias.
func TestRenderParseRoundTrip(t *testing.T) {
	in := []Format{
		{Name: "markdown", ImportPath: "github.com/gofuego/fuego/parsers/markdown", Symbol: "markdown"},
		{Name: "openapi", ImportPath: "github.com/gofuego/fuego-formats/openapi", Symbol: "openapi"},
		{Name: "fuego-terraform", ImportPath: "github.com/acme/fuego-terraform", Symbol: "terraform"},
	}
	dir := t.TempDir()
	if err := WriteFile(dir, in); err != nil {
		t.Fatal(err)
	}

	src, _ := os.ReadFile(filepath.Join(dir, FileName))
	for _, want := range []string{
		"Code generated by fuego; DO NOT EDIT.",
		"func registerFormats(eng *engine.Engine)",
		"eng.Register(markdown.Parser())",
		"eng.Register(openapi.Parser())",
		"eng.Register(terraform.Parser())",
		"terraform \"github.com/acme/fuego-terraform\"",
	} {
		if !strings.Contains(string(src), want) {
			t.Errorf("formats.go missing %q:\n%s", want, src)
		}
	}

	got, err := ParseFile(dir)
	if err != nil {
		t.Fatal(err)
	}
	if !reflect.DeepEqual(got, in) {
		t.Errorf("round trip = %+v, want %+v", got, in)
	}
}

